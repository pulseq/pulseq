    'barDurationSeconds', barDurationSeconds, ...
    'pulseqUseWave', pulseqUseWave, ...
    'axesOrder', 'zxy');

%% output
if pulseqUseWave
    seq.setDefinition('Name', 'badinerie');
    seq.write('badinerie.seq');
else
    seq.setDefinition('Name', 'badinerie1');
    seq.write('badinerie1.seq');
end

return
